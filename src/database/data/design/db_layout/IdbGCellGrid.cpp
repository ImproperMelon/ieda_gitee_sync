IdbGCellGrid::IdbGCellGrid()
{
  _direction = IdbTrackDirection::kNone;
  _start = -1;
  _num = -1;
  _space = -1;
}

IdbGCellGrid::~IdbGCellGrid()
{
}

void IdbGCellGrid::set_direction(std::string direction)
{
  _direction = IdbEnum::GetInstance()->get_layer_property()->get_track_direction(direction);
}

